package Polymorphism;

public class Demo 
{
	
	public void m1()         //non-static method declaration
	{
		System.out.println("running method m1");      //non-static method definition/body
	}
	
	public static void m2() 	 //static method declaration
	{
		System.out.println("running method m2");    //static method definition/body
	}

}
